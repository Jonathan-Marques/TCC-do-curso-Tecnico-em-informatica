    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        String nome = "" + jTable1.getValueAt(jTable1.getSelectedRow(), 1);
        con.conexao();
        
        con.executaSQL("select * from fornecedor where fornecedor_nome='"+ nome+"'");
        try {
           con.rs.first();
            jTextFieldCod.setText(String.valueOf(con.rs.getInt("id_fornecedor")));
            jTextFieldNome.setText(con.rs.getString("fornecedor_nome"));
            jTextFieldEndereco.setText(con.rs.getString("fornecedor_endereco"));
            jTextFieldBairro.setText(con.rs.getString("fornecedor_bairro"));
            jTextFieldCidade.setText(con.rs.getString("fornecedor_cidade"));
            jTextFieldEstado.setText(con.rs.getString("fornecedor_estado"));
            jFormattedTextFieldCnpj.setText(con.rs.getString("fornecedor_cnpj"));
            jFormattedTextFieldTelefone.setText(con.rs.getString("fornecedor_telefone"));
            
            jButtonCancelar.setEnabled(true);
            jButtonExcluir.setEnabled(true);
            jButtonEditar.setEnabled(true);
            
        } catch (SQLException ex) {
            Logger.getLogger(PesqMedico.class.getName()).log(Level.SEVERE, null, ex);
                       
        }
        
        con.desconecta();
        
        
        
    }//GEN-LAST:event_jTable1MouseClicked

