    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        String nomeanimal = "" + jTable1.getValueAt(jTable1.getSelectedRow(), 3);
        con.conexao();
        
        con.executaSQL("select * from animal where nome_animal='"+ nomeanimal+"'");
        try {
            con.rs.first();
            jTextFieldPaciente.setText(con.rs.getString("nome_animal"));
            
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao leva dados");
                       
        }
        
        con.desconecta();
        
      jTextFieldMotivo.setEnabled(true);
      jButtonRealizarAgendamento.setEnabled(true);
      jComboBoxTurno.setEnabled(true);
      jComboBoxVeterinario.setEditable(true);
      jDateChooserData.setEnabled(true);
      jButtonCancelarAgendamento.setEnabled(!false);
       
        
        
    }//GEN-LAST:event_jTable1MouseClicked

