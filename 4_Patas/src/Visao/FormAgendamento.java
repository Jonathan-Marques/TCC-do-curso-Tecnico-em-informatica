    public void preencherVeterinario(){
      con.conexao();
      con.executaSQL("select * from medicos order by nome");
      try{
          con.rs.first();
          jComboBoxVeterinario.removeAllItems();
          do{
              jComboBoxVeterinario.addItem(con.rs.getString("nome"));
            }while(con.rs.next());
          
      } catch (SQLException ex) {
          JOptionPane.showMessageDialog(rootPane,"Nenhum Veterinario Cadastrado");
      }
      con.desconecta();
    }
    
    
    private void preecherTabela(String SQL){
    ArrayList dados = new ArrayList();
    String [] Colunas = new String[]{"Nome","Telefone","CPF","Nome do Animal",};
    con.executaSQL(SQL);
         try {
             con.rs.first();
             do{
                 dados.add(new Object[]{con.rs.getString("nome_proprietario"),con.rs.getString("telefone"),con.rs.getString("cpf"),con.rs.getString("nome_animal") });
                }while(con.rs.next());
         } catch (SQLException ex) {
             JOptionPane.showMessageDialog(null, " Paciente Não Cadastrado! ");
         }
         
   ModeloTabela modelo = new ModeloTabela(dados, Colunas);
   jTable1.setModel (modelo);
   jTable1.getColumnModel().getColumn(0).setPreferredWidth(150);
   jTable1.getColumnModel().getColumn(0).setResizable(false);
   jTable1.getColumnModel().getColumn(1).setPreferredWidth(80);
   jTable1.getColumnModel().getColumn(1).setResizable(false);
   jTable1.getColumnModel().getColumn(2).setPreferredWidth(100);
   jTable1.getColumnModel().getColumn(2).setResizable(false);
   jTable1.getColumnModel().getColumn(3).setPreferredWidth(150);
   jTable1.getColumnModel().getColumn(3).setResizable(false);
   jTable1.getTableHeader().setReorderingAllowed(false);
   jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_OFF);
   jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
