    private void jTablePsqUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePsqUsuarioMouseClicked
        // TODO add your handling code here:
        
        String nome = "" + jTablePsqUsuario.getValueAt(jTablePsqUsuario.getSelectedRow(), 1);
        con.conexao();
        
        con.executaSQL("select * from usuarios where use_usuario='"+ nome+"'");
        try {
            con.rs.first();
            jTextFieldCod.setText(String.valueOf(con.rs.getInt("use_cod")));
            jTextFieldUsuario.setText(con.rs.getString("use_usuario"));
            jPasswordFieldCon_Senha_Usuario.setText(con.rs.getString("use_senha"));
            jPasswordFieldSenha_Usuario.setText(con.rs.getString("use_senha"));
            jComboBoxTipo.setSelectedItem(con.rs.getString("use_tipo"));
            jButtonAltera.setEnabled(true);
            jButtonExcluir.setEnabled(true);
            jButtonCancelar.setEnabled(true);
            jButtonPsq.setEnabled(true);
            jTextFieldNome.setEnabled(true);
            jTextFieldUsuario.setEnabled(false);
            jPasswordFieldCon_Senha_Usuario.setEnabled(false);
            jPasswordFieldSenha_Usuario.setEnabled(false);
            jComboBoxTipo.setEnabled(false);
            
            
        } catch (SQLException ex) {
            Logger.getLogger(PesqMedico.class.getName()).log(Level.SEVERE, null, ex);
                       
        }
        
        con.desconecta();
        
    }//GEN-LAST:event_jTablePsqUsuarioMouseClicked

    private void preecherTabela(String SQL){
    ArrayList dados = new ArrayList();
    String [] Colunas = new String[]{"ID","Usuario","Tipo",};
    con.executaSQL(SQL);
         try {
             con.rs.first();
             do{
                 dados.add(new Object[]{con.rs.getInt("use_cod"),con.rs.getString("use_usuario"),con.rs.getString("use_tipo") });
                }while(con.rs.next());
         } catch (SQLException ex) {
             }
         
   ModeloTabela modelo = new ModeloTabela(dados, Colunas);
   jTablePsqUsuario.setModel (modelo);
   jTablePsqUsuario.getColumnModel().getColumn(0).setPreferredWidth(50);
   jTablePsqUsuario.getColumnModel().getColumn(0).setResizable(false);
   jTablePsqUsuario.getColumnModel().getColumn(1).setPreferredWidth(250);
   jTablePsqUsuario.getColumnModel().getColumn(1).setResizable(false);
   jTablePsqUsuario.getColumnModel().getColumn(2).setPreferredWidth(166);
   jTablePsqUsuario.getColumnModel().getColumn(2).setResizable(false);
   jTablePsqUsuario.getTableHeader().setReorderingAllowed(false);
   jTablePsqUsuario.setAutoResizeMode(jTablePsqUsuario.AUTO_RESIZE_OFF);
   jTablePsqUsuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    
    }
    
