                jButtonCancelar.setEnabled(false);
                jButtonAltera.setEnabled(false);
                jButtonExcluir.setEnabled(false);
                jButtonPsq.setEnabled(true);
                jTextFieldNome.setEnabled(true);
                jTextFieldNome.setText("");
                jTextFieldUsuario.setText("");
                jPasswordFieldCon_Senha_Usuario.setText("");
                jPasswordFieldSenha_Usuario.setText("");
        con.conexao();
        preecherTabela("select * from usuarios");
